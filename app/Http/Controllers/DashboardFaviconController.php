     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\RedirectResponse
     */
    public function update(Request $request, $id)
    {
        $favicon = Logo::find($id);

        if($request->hasFile("favicon")) {
            $imageName=time()."-"."favicon".".".$request->favicon->getClientOriginalExtension();
            $request->favicon->move(public_path("img/logo"),$imageName);
            $favicon->favicon=$imageName;
        }

        $favicon->save();
        return redirect()->route("dashboard-logo.index")->with("success", "Favicon başarılı bir şekilde değiştirildi.");
    }

